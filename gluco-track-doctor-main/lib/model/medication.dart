class Medication {
  final String medicineName;
  final String dosage;
  final String duration;
  final String time;
  final String doseInterval;
  final String notes;

  Medication({
    required this.medicineName,
    required this.dosage,
    required this.duration,
    required this.time,
    required this.doseInterval,
    required this.notes,
  });
}
